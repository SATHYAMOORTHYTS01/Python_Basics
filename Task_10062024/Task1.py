#4.The ^ Operator in Python
#The ^ operator is the bitwise XOR (exclusive OR) operator.
# It is used to compare each bit of two numbers and returns a new number whose bits are set to 1 where the corresponding bits of either but not both operands are 1.
# For example, 5 ^ 3 results in 6 because the binary representation of 5 is 101 and 3 is 011, and the XOR operation results in 110 which is 6 in decimal.