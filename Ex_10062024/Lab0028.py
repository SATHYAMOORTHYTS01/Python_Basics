 # Arithmatic Operators
# +, -, *, /, %, //, **

a = 180
b = 90
print(a + b) # 15
print(a - b) # 5
print(a * b) # 50
print(a / b) # 2.0 - # Always comes with float
print(a % b) # 0 -  Remainder
print(a // b) # 2 - Quotient
print(a ** b)


# Modulus - operator returns the remainder of the division
# 5 | 10 | 2
#   | 10 |
#   | 0  |
print(10 % 3) # 1
print(10 % 4) # 2
print(10 % 5) # 0



